public class LokalesEssen {
    String name;
    int preis;
    
    public LokalesEssen(String name, int preis){
        this.name=name;
        this.preis=preis;
    }

    public String getName() {
        return name;
    }

    public int getPreis() {
        return preis;
    }
    
    
}
